    public List<String> ipPortPairs(List<ESBNode> esbNodes) {
        List ipPortPairs = new ArrayList();
        for (Iterator<ESBNode> i = esbNodes.iterator(); i.hasNext();) {
            ipPortPairs.add(i.next().getIpAndPort());
        }
        return ipPortPairs;
    }

