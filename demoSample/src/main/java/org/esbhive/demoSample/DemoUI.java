    private int findIndex(String ipAndPort) {
        Iterator<JButton> iterator = buttonList.iterator();
        JButton next = null;
        int index = 0;
        while (iterator.hasNext()) {
            next = iterator.next();
            if (ipAndPort.equals(next.getText())) {
                index = buttonList.indexOf(next);

            }
        }
        return index;
    }

